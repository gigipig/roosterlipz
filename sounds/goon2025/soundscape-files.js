// Build peaceful vs non-peaceful lists from the full set without losing any files.
(function () {
  try {
    var nonPeacefulPatterns = [
      /attack/i,
      /\bdie\b/i,
      /destroy/i,
      /destroyed/i
    ];
    var ambiencePatterns = [
      /birds?/i,
      /\bwind\b/i,
      /\bfish/i,
      /fire/i,
      /forest/i,
      /grass/i,
      /lake/i,
      /ocean/i
    ];
    function isNonPeaceful(name) {
      for (var i = 0; i < nonPeacefulPatterns.length; i++) {
        if (nonPeacefulPatterns[i].test(name)) return true;
      }
      return false;
    }
    function isAmbience(name) {
      for (var i = 0; i < ambiencePatterns.length; i++) {
        if (ambiencePatterns[i].test(name)) return true;
      }
      return false;
    }
    var all = Array.isArray(window.SOUND_FILES) ? window.SOUND_FILES : [];
    var peaceful = [];
    var nonpeaceful = [];
    var ambience = [];
    for (var i = 0; i < all.length; i++) {
      var f = String(all[i]);
      if (isAmbience(f)) ambience.push(f);
      if (isNonPeaceful(f)) {
        nonpeaceful.push(f);
      } else {
        peaceful.push(f);
      }
    }
    window.SOUND_FILES_PEACEFUL = peaceful;
    window.SOUND_FILES_NONPEACEFUL = nonpeaceful;
    window.SOUND_FILES_AMBIENCE = ambience;
  } catch (e) {
    // no-op
  }
})();
